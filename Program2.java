package Programs;

public class Program2 {
	static boolean b;
	static byte bi;
	static short s;
	static int i;
	static float f;
	static double d;
	static long l;
	static char ch;
	static String str;
	public static void main(String[] args) {		
		System.out.println("Default value of boolean is "+b);
		System.out.println("Default value of byte is    "+bi);
		System.out.println("Default value of short is   "+s);
		System.out.println("Default value of int is     "+i);
		System.out.println("Default value of float is   "+f);
		System.out.println("Default value of double is  "+d);
		System.out.println("Default value of long is    "+l);
		System.out.println("Default value of char is    "+ch);
		System.out.println("Default value of String is  "+str);
	}
}
